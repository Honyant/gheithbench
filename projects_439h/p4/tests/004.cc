#define SIZE 100000

/*
    promotion failure

    this test ensures that you reuse resources when promoting weak
    pointers. you will run out of space if you don't.
 */

// a class
struct BagelShop {
  uint32_t bagels;

  BagelShop(uint32_t bagels) : bagels{bagels} {}
};

// a container
auto treasure_chest = new StrongPtr<BagelShop>[SIZE];

void kernelMain() {
  // youve started
  Debug::printf("*** WOKE UP FEELIN BAGEL-Y\n");

  // set up
  StrongPtr<BagelShop> bagels{new BagelShop(2027)}; // this is never used again
  WeakPtr<BagelShop> weak_bagel{bagels};
  bagels = nullptr;

  // sorry...
  for (uint32_t i = 0; i < SIZE; ++i)
    treasure_chest[i] = weak_bagel.promote();

  // can you get here?
  Debug::printf("*** ELIE HAS %u BAGELS\n", SIZE);
}

